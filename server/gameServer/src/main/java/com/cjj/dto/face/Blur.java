package com.cjj.dto.face;

public class Blur{
    private String blurLevel;
	private float value;
	
	public Blur() {
		super();
	}
	public String getBlurLevel() {
		return blurLevel;
	}
	public void setBlurLevel(String blurLevel) {
		this.blurLevel = blurLevel;
	}
	public float getValue() {
		return value;
	}
	public void setValue(float value) {
		this.value = value;
	}
	
}