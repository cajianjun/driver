package com.cjj.dto.face;

public class Noise{
	private String noiseLevel;
	private float value;
	
	public Noise() {
		super();
	}
	public String getNoiseLevel() {
		return noiseLevel;
	}
	public void setNoiseLevel(String noiseLevel) {
		this.noiseLevel = noiseLevel;
	}
	public float getValue() {
		return value;
	}
	public void setValue(float value) {
		this.value = value;
	}
	
}
